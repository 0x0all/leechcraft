	struct DBoxItem
	{
		QString Id_;
		QString ParentID_;

		QString Name_;
		quint64 FileSize_;
		bool IsFolder_;
		bool IsDeleted_;
		QString FolderHash_;
		QDateTime ModifiedDate_;
		QString MimeType_;

		DBoxItem ()
		: FileSize_ (0)
		, IsFolder_ (false)
		{
		}

		bool operator== (const DBoxItem& item) const
		{
			return Id_ == item.Id_;
		}
	};

